let value = 1;

if (value == 1) {
    console.log("sunday");
} else if (value == 2) {
    console.log("monday")
} else if (value == 3) {
    console.log("tuesday");
} else if (value == 4) {
    console.log("wednesday");
} else if (value == 5) {
    console.log("thursday");
} else if (value == 6) {
    console.log("friday");
} else if (value == 7) {
    console.log("saturday");
} else {
    console.log("number out of bound");
}